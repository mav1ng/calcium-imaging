def poly_lr(iter, maxiter, base_lr, exp=0.9):
    """
    Method that implements the poly learning rate policy
    :param iter: number of current iteration
    :param maxiter: max number of iterations in training
    :param base_lr: base learning rate
    :param exp: exp factor
    :return: current learning rate
    """
    return base_lr * np.power((1 - iter / maxiter), exp)


